    private void loadMenuData() {
        menuPanel.removeAll();
        
        // Minta data ke Controller (Controller yang kerja ambil dari DB)
        List<Menu> menus = menuController.getAvailableMenus(idCabang);
        
        // View tinggal menampilkan hasilnya
        for (Menu m : menus) {
            addMenuCard(m);
        }
        
        menuPanel.revalidate();
        menuPanel.repaint();
    }
    
    private void addMenuCard(Menu m) {
        JPanel card = new JPanel();
        card.setLayout(new BoxLayout(card, BoxLayout.Y_AXIS));
        card.setBorder(BorderFactory.createCompoundBorder(
            BorderFactory.createLineBorder(new Color(200,200,200), 1),
            BorderFactory.createEmptyBorder(10, 10, 10, 10)
        ));
        card.setBackground(Color.WHITE);

        JLabel lblNama = new JLabel(m.getName());
        lblNama.setFont(new Font("Segoe UI", Font.BOLD, 14));
        lblNama.setAlignmentX(Component.CENTER_ALIGNMENT);
        
        JLabel lblHarga = new JLabel("Rp " + (int)m.getPrice());
        lblHarga.setAlignmentX(Component.CENTER_ALIGNMENT);
        
        JLabel lblKet = new JLabel("<html><center>Klik untuk Detail & Order</center></html>");
        lblKet.setForeground(Color.BLUE);
        lblKet.setCursor(new Cursor(Cursor.HAND_CURSOR));
        lblKet.setAlignmentX(Component.CENTER_ALIGNMENT);
        
        // Event Klik
        lblKet.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent evt) {
                showDetailDialog(m);
            }
        });

        card.add(Box.createVerticalStrut(10));
        card.add(lblNama);
        card.add(lblHarga);
        card.add(Box.createVerticalStrut(5));
        card.add(lblKet);
        card.add(Box.createVerticalStrut(10));

        menuPanel.add(card);
    }
    
    private void showDetailDialog(Menu m) {
        // 1. Ambil data Add-on dari Controller
        java.util.List<model.AddOn> addons = menuController.getAddOnsForMenu(m.getId());

        // 2. Kirim data menu DAN add-on ke Dialog
        MenuDetailDialog dialog = new MenuDetailDialog(this, m.getName(), m.getPrice(), m.getDescription(), addons);
        dialog.setVisible(true);

        if (dialog.isConfirmed) {
            String details = dialog.selectedSize + ", " + dialog.selectedSugar + ", " + dialog.selectedIce;
            
            // Tambahkan info Addon ke string detail tampilan
            if (!dialog.selectedAddOns.isEmpty()) {
                details += "<br>+ ";
                for (model.AddOn ad : dialog.selectedAddOns) {
                    details += ad.getName() + ", ";
                }
                // Hapus koma terakhir
                details = details.substring(0, details.length() - 2);
            }
            
            if(dialog.isTumbler) details += " (Tumbler)";
            
            // 3. Masukkan ke Keranjang (Update Constructor CartItem)
            cartList.add(new CartItem(
                m.getId(), 
                m.getName(),                
                details,                    
                dialog.finalPrice,          
                dialog.selectedNote,
                dialog.selectedAddOns // <-- Kirim List Addon ke CartItem
            ));
            
            updateCartUI();
        }
    }
    
    private void updateCartUI() {
        keranjangPanel.removeAll();
        keranjangPanel.setLayout(new GridLayout(0, 1, 0, 5));
        double subTotal = 0; // Harga murni barang

        // 1. Hitung Subtotal dari Item Keranjang
        for (int i = 0; i < cartList.size(); i++) {
            CartItem item = cartList.get(i);
            int idx = i;
            
            JPanel itemPanel = new JPanel(new BorderLayout());
            // Beri ukuran fix agar terlihat
            itemPanel.setPreferredSize(new Dimension(200, 60)); 
            itemPanel.setBackground(Color.WHITE);
            itemPanel.setBorder(BorderFactory.createMatteBorder(0, 0, 1, 0, Color.LIGHT_GRAY));

            // Tampilan Info Item
            String detailText = "<html><b>" + item.getNamaMenu() + "</b><br><small>" + item.getDetail();
            // Tampilkan Note jika ada
            if(!item.getNote().isEmpty()) detailText += "<br>Note: " + item.getNote();
            detailText += "</small></html>";
            
            JLabel lblInfo = new JLabel(detailText);
            lblInfo.setBorder(new EmptyBorder(5, 5, 5, 0));
            
            JPanel rightPanel = new JPanel(new FlowLayout(FlowLayout.RIGHT));
            rightPanel.setBackground(Color.WHITE);
            JLabel lblHarga = new JLabel("Rp " + (int)item.getHarga());
            
            JButton btnDel = new JButton("X");
            btnDel.setBackground(Color.RED);
            btnDel.setForeground(Color.WHITE);
            btnDel.setMargin(new Insets(2, 5, 2, 5));
            btnDel.setFont(new Font("Arial", Font.BOLD, 10));
            
            btnDel.addActionListener(e -> {
                cartList.remove(idx);
                updateCartUI();
            });
            
            rightPanel.add(lblHarga);
            rightPanel.add(btnDel);

            itemPanel.add(lblInfo, BorderLayout.CENTER);
            itemPanel.add(rightPanel, BorderLayout.EAST);
            keranjangPanel.add(itemPanel);
            
            // Akumulasi Subtotal
            subTotal += item.getHarga(); // Harga item sudah termasuk add-on
        }

        // 2. LOGIC HITUNG DISKON BERTUMPUK (STACKING)
        currentDiscount = 0; // Reset
        
        if (activeDiscounts != null) {
            for (model.Discount d : activeDiscounts) {
                // Cek syarat minimal belanja
                if (subTotal >= d.getMinBelanja()) {
                    double potonganIni = 0;
                    
                    if (d.getTipe().equalsIgnoreCase("nominal")) {
                        potonganIni = d.getNilai();
                    } else if (d.getTipe().equalsIgnoreCase("persen")) {
                        // Persen dari Subtotal Murni (Bukan yang sudah dipotong)
                        potonganIni = subTotal * (d.getNilai() / 100); 
                    }
                    
                    currentDiscount += potonganIni; // Tambahkan ke tumpukan diskon
                }
            }
        }
        
        // Cegah diskon minus (lebih besar dari harga)
        if (currentDiscount > subTotal) {
            currentDiscount = subTotal;
        }

        finalTotal = subTotal - currentDiscount; // Total Akhir
        grandTotal = finalTotal; // Update variabel global lama biar kompatibel

        // 3. Tampilkan Info Rinci
        String textTotal = "<html>Subtotal: Rp " + (int)subTotal;
        if (currentDiscount > 0) {
            textTotal += "<br><font color='green'>Hemat: -Rp " + (int)currentDiscount + "</font>";
        }
        textTotal += "<br><b>Total Bayar: Rp " + (int)finalTotal + "</b></html>";
        
        totalLable.setText(textTotal); // Update Label UI
        
        keranjangPanel.revalidate();
        keranjangPanel.repaint();
    }
    
